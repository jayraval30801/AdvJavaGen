package com.controller;


public class ControlStatementEx {
	public static void main(String[] args)
	{
		int i =1,j;
		for(i=5;i>=1;i--) //i=4
		{
			for(j=1;j<=i;j++) //j=1 j=2 j=3 j=4  i=4 5<=5  
			{
				System.out.print("*");
			}
			System.out.println();
		}
	}	
}

